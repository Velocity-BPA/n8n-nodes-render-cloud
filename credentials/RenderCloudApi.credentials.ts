import type {
	IAuthenticateGeneric,
	ICredentialTestRequest,
	ICredentialType,
	INodeProperties,
} from 'n8n-workflow';

export class RenderCloudApi implements ICredentialType {
	name = 'renderCloudApi';
	displayName = 'Render Cloud API';
	documentationUrl = 'https://render.com/docs/api';
	properties: INodeProperties[] = [
		{
			displayName: 'API Key',
			name: 'apiKey',
			type: 'string',
			typeOptions: {
				password: true,
			},
			default: '',
			required: true,
			description: 'API key from Render Dashboard Account Settings. Navigate to Account Settings > API Keys to create one.',
		},
	];

	authenticate: IAuthenticateGeneric = {
		type: 'generic',
		properties: {
			headers: {
				Authorization: '=Bearer {{$credentials.apiKey}}',
			},
		},
	};

	test: ICredentialTestRequest = {
		request: {
			baseURL: 'https://api.render.com/v1',
			url: '/owners',
			method: 'GET',
		},
	};
}
